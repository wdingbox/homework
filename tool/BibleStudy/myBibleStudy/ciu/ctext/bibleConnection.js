告子上:	
告子曰：「食色，性也。仁，內也，非外也；義，外也，非內也。」

惻隱之心，仁也；羞惡之心，義也；恭敬之心，禮也；是非之心，智也。

仁，人心也；義，人路也。

仁者無不愛也


盡心下:	
孟子曰：「不仁而得國者，有之矣；不仁而得天下，未之有也。」

孟子曰：「仁也者，人也。合而言之，道也。」


仁之於父子也，義之於君臣也，


