function gen_trs(tds){
	var tb="";
	$.each(I,function(k,v){
		var kjv=K[k];
		var vers=k.substr(4).replace("_",":");
		tb+="<tr><td></td><td title='"+kjv+"'>"+vers+"</td><td>";
		tb+=hili_keyword(N[k])+"<br>";
		tb+=v+"<br>";
		tb+=hili_keyword(H[k]);
		tb+="</td>";
		tb+=tds;
	});

	$("#tbd").append(tb);
	return tb;
}
function hili_keyword(vers){
	$.each(KeyWordGrps,function(k,ar){
		var ac=" <a class='the "+k+"'>";
		var regexp = new RegExp("(^|[^a-z])"+k, "g");
		vers=vers.replace(regexp, ac+k+"</a>");
		$.each(ar,function(i,v){
			regexp = new RegExp(""+v, "g");
			vers=vers.replace(regexp, ac+v+"</a>");
		});
	});
	return vers;
}
function print_KeyWordGrps(){
	var a="";
	$.each(KeyWordGrps,function(k,ar){
		var ac="<a class='the "+k+"'>";
		a+=" || "+ac+k+"</a>";
		$.each(ar,function(i,v){
			a+=" , "+ ac + v +"</a>";
		});
	});
	return a;
}
